"use client";

import "./globals.css";
import type { Metadata } from "next";
import { Poppins } from "next/font/google";
import { usePathname, useRouter } from "next/navigation";
import LandingPageHeader from "@/components/LandingPage/header";
import { GlobalQueryClientProvider } from "@/providers/query-client-provider";
import { ThemeProvider } from "@/providers/theme-provider";
import { ToastProvider } from "@/providers/toaster-provider";
import AuthProvider from "@/providers/auth-provider";

const poppins = Poppins({
  weight: ["400", "500", "600", "700", "800", "900"],
  subsets: ["latin"],
});

const metadata: Metadata = {
  title: "Coursewave",
  description: "Generated by create next app",
  icons: {
    icon: "/courseWaveFaviconColored.ico",
    shortcut: "/courseWaveFaviconColored.ico",
  },
};

export default function RootLayout({
  children,
}: {
  children: React.ReactNode;
}) {
  const router = useRouter();
  const pathname = usePathname();

  const handleLoginClick = () => {
    router.push("/login");
  };

  const hideSidebar = pathname.match(
    /courses\/enrolledCourses\/(undefined|null)/
  );

  return (
    <html lang="en">
      {/* <AuthProvider> */}
        <body className={poppins.className}>
          {/* <CookiesProvider> */}
          <GlobalQueryClientProvider>
            <ThemeProvider
              attribute="class"
              defaultTheme="system"
              enableSystem
              disableTransitionOnChange
            >
              <ToastProvider />

              <div className="dark:bg-zinc-800">
                {hideSidebar && (
                  <LandingPageHeader handleLoginClick={handleLoginClick} />
                )}
                {children}
              </div>
            </ThemeProvider>

            {/* </Provider> */}
          </GlobalQueryClientProvider>
          {/* </CookiesProvider> */}
        </body>
      {/* </AuthProvider> */}
    </html>
  );
}
